
public class Food {
	int cal;
	int cost;
	int kg;
	
	public Food () {
		
	}
	
	public void setCal(int cal) {
		this.cal = cal;
	}
	public int getCal() {
		return cal;
	}
	
	public void setCost(int cost) {
		this.cost = cost;
	}
	public int getCost() {
		return cost;
	}
	public void setKg(int kg) {
		this.kg = kg;
	}
	public int getKg() {
		return kg;
	}
}
